class MulNumbers_1_to_5 
{
	public static void main(String[] args) 
	{
		int mul=1;
		for(int i=0; i<=5; i++)
		{
			mul = mul * i;
			System.out.println("multiply values adding in container like  "+mul);
			
		}
		 
		System.out.println("..................Multiplied result is................................................: " +mul);
	}
}
