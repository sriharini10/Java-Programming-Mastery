class Concatenation 
{
	public static void main(String[] args) 
	{
		System.out.println("java" + "script"); //javascript
		
		System.out.println("java" + 10);       //java10
		
		System.out.println(10+ "script");      //10java
		
		System.out.println("java" + 10 + 20);  //java1020
		
		System.out.println(10 + 20 + "script"); //30script
		
	}
}
