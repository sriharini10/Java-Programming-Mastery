class DatatypeEg 
{
	public static void main(String[] args) 
	{
		String name="aaa";
		System.out.println("Student name is:" +name);
		
		byte age=21;
		System.out.println("Student age is:" +age);
		
		short marks=450;
		System.out.println("Student mark is:" +marks);
		
		int salary=50000;
		System.out.println("Student salary is:" +salary);
		
		long phNo=919876543210L;
		System.out.println("Student phone number is:" +phNo);
		
		float height=5.5f;
		System.out.println("Student height is:" +height);
		
		double cgpa=8.85;
		System.out.println("Student cgpa is:" +cgpa);
		
		char gender='f';
		System.out.println("Student gender is:" +gender);
		
		boolean isMarried=false;
		System.out.println("You are Married:" +isMarried);
	}
}
